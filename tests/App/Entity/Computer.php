namespace araise\SearchBundle\Tests\App\Entity;

use araise\SearchBundle\Annotation\Index;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Validator\Constraints as Assert;

#[ORM\Table(name: 'computer')]
#[ORM\Entity]
class Computer
{
    #[ORM\Column(type: 'integer')]
    #[ORM\Id]
    #[ORM\GeneratedValue(strategy: 'AUTO')]
    private ?int $id = null;

    #[Index]
    #[ORM\Column(type: 'string')]
    #[Assert\NotBlank]
    #[Assert\NotNull]
    private ?string $name = null;

    #[ORM\OneToMany(mappedBy: 'computer', targetEntity: Disk::class)]
    private Collection $disks;

    public function __construct()
    {
        $this->disks = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function setId(?int $id): void
    {
        $this->id = $id;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(?string $name): void
    {
        $this->name = $name;
    }

    public function getDisks(): Collection
    {
        return $this->disks;
    }

    public function setDisks(Collection $disks): void
    {
        $this->disks = $disks;
    }
}
